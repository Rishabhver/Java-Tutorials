package SuperKeywordConcept;

public class A {
	
	// constructor
	
	public A()
	{
		System.out.println("Parent class constructor");
	}
	
	public A(int i)
	{
		System.out.println("Parent paran const with 1 value -->" +i);
	}
	
	public A(int i, int j)
	{
		System.out.println("Parent paran const with 2  param, 1st is  -->" +i);
		System.out.println("Parent paran const with 2  param, 2nd is  -->" +j);
	}

}
